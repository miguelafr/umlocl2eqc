-module(ocl_gen).

-include_lib("eqc/include/eqc.hrl").

%% API
-export([gen_integer/0, gen_unlimitednatural/0, gen_sequence_integer/0,
         gen_sequence/1, gen_string/0]).

%%====================================================================
%% API
%%====================================================================
%%--------------------------------------------------------------------
%% generator: integer
%%--------------------------------------------------------------------
gen_integer() ->
    eqc_gen:int().

%%--------------------------------------------------------------------
%% generator: nat
%%--------------------------------------------------------------------
gen_unlimitednatural() ->
    eqc_gen:nat().

%%--------------------------------------------------------------------
%% generator: sequence
%%--------------------------------------------------------------------
gen_sequence(G) ->
    eqc_gen:list(G).

%%--------------------------------------------------------------------
%% generator: sequence_integer
%%--------------------------------------------------------------------
gen_sequence_integer() ->
    gen_sequence(eqc_gen:int()).

%%--------------------------------------------------------------------
%% generator: string
%%--------------------------------------------------------------------
gen_string() ->
    gen_sequence(eqc_gen:char()).