-module(ocl_seq).

%% API
-export([new_seq/2, includes/2, size/1, iterate/3, forAll/2, not_empty/1]).

%%====================================================================
%% API
%%====================================================================
%%--------------------------------------------------------------------
%% new_seq
%%--------------------------------------------------------------------
new_seq(A, B)->
    lists:seq(A, B).

%%--------------------------------------------------------------------
%% includes
%%--------------------------------------------------------------------
includes(E, L)->
    lists:member(E, L).

%%--------------------------------------------------------------------
%% size
%%--------------------------------------------------------------------
size(L)->
    erlang:length(L).

%%--------------------------------------------------------------------
%% iterate
%%--------------------------------------------------------------------
iterate(F, Acc0, L)->
    lists:foldl(F, Acc0, L).

%%--------------------------------------------------------------------
%% forAll
%%--------------------------------------------------------------------
forAll(F, L)->
    lists:all(F, L).

%%--------------------------------------------------------------------
%% not_empty
%%--------------------------------------------------------------------
not_empty(L)->
    erlang:length(L) > 0.