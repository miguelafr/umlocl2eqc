-module(eog_datatype_setlists).

%% API
-export([add_element/2, append/1, append/2, intersection/2, union/2]).

%%====================================================================
%% API
%%====================================================================
%%--------------------------------------------------------------------
%% add_element
%%--------------------------------------------------------------------
add_element(E, L)->
    lists:sort(sets:to_list(sets:from_list([E | L]))).

%%--------------------------------------------------------------------
%% append
%%--------------------------------------------------------------------
append(L) ->
    lists:sort(sets:to_list(sets:from_list(lists:append(L)))).

%%--------------------------------------------------------------------
%% append
%%--------------------------------------------------------------------
append(L1, L2) ->
    lists:sort(sets:to_list(sets:from_list(L1 ++ L2))).

%%--------------------------------------------------------------------
%% intersect
%%--------------------------------------------------------------------
intersection(L1, L2) ->
    lists:sort(sets:to_list(sets:intersection(sets:from_list(L1), sets:from_list(L2)))).

%%--------------------------------------------------------------------
%% union
%%--------------------------------------------------------------------
union(L1, L2) ->
    lists:sort(sets:to_list(sets:union(sets:from_list(L1), sets:from_list(L2)))).