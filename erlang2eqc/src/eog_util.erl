-module(eog_util).

%% API
-export([line_break/0, foldl/4]).

%%====================================================================
%% API
%%====================================================================
%%--------------------------------------------------------------------
%% line_break
%%--------------------------------------------------------------------
line_break()->
    "\r\n".

%%--------------------------------------------------------------------
%% foldl
%%--------------------------------------------------------------------
foldl(Init, Args, FArgs, FSep)->
    lists:foldl(fun(Arg, Result) ->
            case Result of
                "" ->
                    FArgs(Arg);
                _ ->
                    Result ++ FSep() ++ FArgs(Arg)
            end
    end, Init, Args).